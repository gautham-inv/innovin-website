// Helper function to get logo URL with fallback
export function getTechLogo(techKey: string): string {
  const tech = techLogosData[techKey as keyof typeof techLogosData];
  if (!tech) return '';
  return tech?.url || tech?.fallback || '';
}

// Helper function to get tech name
export function getTechName(techKey: string): string {
  const tech = techLogosData[techKey as keyof typeof techLogosData];
  return tech?.name || techKey;
}

// Helper function to check if logo exists
export function hasValidLogo(techKey: string): boolean {
  const tech = techLogosData[techKey as keyof typeof techLogosData];
  return !!(tech?.url || tech?.fallback);
}
